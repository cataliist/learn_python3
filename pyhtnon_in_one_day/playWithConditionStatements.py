"""
Disclaimer: Most common condition statements are demonstrated
"""

#is equal to
print(5==5)
print(5==6)

#is not equal to
print(5!=5)
print(5!=6)

#is greater than or equal to
print(5>=5)
print(5>6)

#is less than or equal to
print(5<=5)
print(5<6)

"""
Logical operators
"""

#and
print(5==5 and 5==10)
print(5<6 and 5>2)

#or
print(5==5 or 5==10)
print(5<6 or 5>2)

#not
print(not 5==10)
print(not 5>2)


